    private void atualizaClick(String peca) {
        switch(peca){
            case "A_A":
                this.A_A.setIcon(this.controller.getIconPeca(0, 0));
                break;
            case "B_B":
                this.B_B.setIcon(this.controller.getIconPeca(1, 1));
                break;
            case "C_C":
                this.C_C.setIcon(this.controller.getIconPeca(2, 2));
                break;
            case "A_B":
                this.A_B.setIcon(this.controller.getIconPeca(0,1));
                break;
            case "A_C":
                this.A_C.setIcon(this.controller.getIconPeca(0, 2));
                break;
            case "B_A": this.B_A.setIcon(this.controller.getIconPeca(1, 0));
                break;
            case "B_C":
                this.B_C.setIcon(this.controller.getIconPeca(1, 2));
                break;
            case "C_A":
                this.C_A.setIcon(this.controller.getIconPeca(2, 0));
                break;
            case "C_B":
                this.C_B.setIcon(this.controller.getIconPeca(2, 1));
                break;
        }
    }
