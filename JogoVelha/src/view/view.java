    private void atualizaLabels(){
        this.textFeedback.setText("<html>"+controller.getFeedback()+"</html>");
        this.textJogador.setText("<html>"+controller.getJogadorAtual()+"</html>");
        this.textVitoriaJogador1.setText("<html>"+controller.getJogo().getVitoriasJogadorUm()+"</html>");
        this.textVitoriaJogador2.setText("<html>"+controller.getJogo().getVitoriasJogadorDois()+"</html>");
        ganhou = this.controller.isGanhou();
    }

    public void resetTabuleiro(){
        String[] linha = {"A", "B", "C"};
        String[] coluna = {"A","B", "C"};
        
        for(String sL: linha){
            for(String sC: coluna){
                atualizaClick(sL+"_"+sC);
            }
        }
        
    }

